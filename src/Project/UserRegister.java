            pst.setString(2,name);
            pst.setString(3,email);
            pst.setInt(4,phoneno);
            pst.setInt(5,aid);
            int r = pst.executeUpdate();
         
            sst = con.prepareStatement("Insert into PROJECT.ULOGIN values (?,?,?)");
