           con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE","project","college");
           
           
            String gst = ("select max(U_ID) as maxi from PROJECT.USERS ");
            PreparedStatement stmt = con.prepareStatement(gst);
            ResultSet d = stmt.executeQuery(gst);
            if (d.next())
            {
               uid = d.getInt("maxi");
               
            }
            uid = uid+1; 
            
            cst = con.prepareStatement("select * from PROJECT.USERS where email = ? ");
